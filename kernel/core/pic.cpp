// kernel/core/pic.cpp
#include "portio.h"

void pic_remap(int offset1, int offset2) {
    outb(0x20, 0x11);
    outb(0xA0, 0x11);
    outb(0x21, offset1);
    outb(0xA1, offset2);
    outb(0x21, 0x04);
    outb(0xA1, 0x02);
    outb(0x21, 0x01);
    outb(0xA1, 0x01);
    outb(0x21, 0x0);
    outb(0xA1, 0x0);
}

void pic_send_eoi(unsigned char irq) {
    if(irq >= 8)
        outb(0xA0, 0x20);
    outb(0x20, 0x20);
}
